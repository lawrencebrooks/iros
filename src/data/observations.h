// Obseration matrix
#define OBSERVATION_MATRIX_SIZE 392
#define OBSERVATION_MATRIX_WIDTH 7
#define OBSERVATION_MATRIX_HEIGHT 56

u8 build_state(u8 orientation, u8 distance_sevenths, u8 posture)
{
	return (orientation << 5) | (distance_sevenths << 2) | posture;
}

void record_observation(u8 state, u8 action)
{
	u8 row = (EEPROM_BLOCK_SIZE*EEPROM_HEADER_SIZE)+(state*OBSERVATION_MATRIX_WIDTH);
	for (u8 i = 0; i < OBSERVATION_MATRIX_WIDTH; i++)
	{
		if (ReadEeprom(row+i) == 255) return;
	}
	WriteEeprom(row+action, ReadEeprom(row+action)+1);
}

